    <h2> Our Uniquiness </h2>
    <p>
        A blood bank is present mostly in every hospital but sometimes there come situations where blood bank doesn’t have that particular blood group which are required by the patient 
        So, for that, we have made a Portal where hospitals who are being registered already can log in and ask for that particular kind of blood to the people who have registered on that portal 
        So as soon as they make a request then within a radius of 5 km that hospital every person who has registered on that portal will receive an email for donating that particular group of blood to the hospital
        By this portal, our mission is to serve our community by meeting the needs of patients, hospitals, and members for safe, high-quality blood products and related services.
    </p>

